#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		sum;
	int		plsmns;

	i = 0;
	sum = 0;
	plsmns = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '-')
	{
		plsmns = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		sum = sum * 10 + (str[i] - 48);
		i++;
	}
	return (sum * plsmns);
}
